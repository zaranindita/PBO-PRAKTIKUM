public class Mobil {
    private String merek;
    private String warna;
    private int tahun;

    public Mobil(String merek, String warna, int tahun) {
        this.merek = merek;
        this.warna = warna;
        this.tahun = tahun;
    }

    public String getMerek() {
        return merek;
    }

    public String getWarna() {
        return warna;
    }

    public int getTahun() {
        return tahun;
    }
}



